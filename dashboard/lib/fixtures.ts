// ---------------------------------------------------------------------------
// Journeys fixtures — each journey's `events` are ordered by timestamp and
// follow the Correlation Model exactly: phase-1 logs carry only eventId, the
// bridge log (logger ResponseListener) carries eventId + the order id(s),
// phase-2 logs carry orderId + cartHeaderId and never eventId. Pre-creation
// failures (order_id/cart_header_id both null) never get order ids, per
// CLAUDE.md invariant 3. The Journeys table's "Stopped At" column is derived
// from the last event's app_name rather than a fabricated field.
// ---------------------------------------------------------------------------

function journeyEvent(ts: string, raw: LogLine): JourneyEvent {
  return { log_id: raw.log_id, ts, raw };
}

